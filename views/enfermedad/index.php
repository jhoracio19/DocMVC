                <div class="imagen-placeholder"></div>
                <h3 class="titulo-azul">Otitis Media</h3>
                <p>Infección del oído medio que causa dolor, fiebre y puede afectar la audición. Es común en niños y se trata con antibióticos.</p>
                <strong>Síntomas principales:</strong>
                <ul>
                    <li>Dolor de oído intenso</li>
                    <li>Fiebre</li>
                    <li>Pérdida temporal de audición</li>
                    <li>Secreción del oído</li>
                </ul>
            </div>

            <!-- Tarjeta 2 -->
            <div class="card-enfermedad">
                <div class="imagen-placeholder"></div>
                <h3 class="titulo-azul">Pérdida Auditiva</h3>
                <p>Disminución de la capacidad para escuchar sonidos. Puede ser temporal o permanente, y afectar uno o ambos oídos.</p>
                <strong>Síntomas principales:</strong>
                <ul>
                    <li>Dificultad para escuchar conversaciones</li>
                    <li>Necesidad de subir el volumen</li>
                    <li>Zumbido en los oídos</li>
                    <li>Sensación de oído tapado</li>
                </ul>
            </div>

            <!-- Tarjeta 3 -->
            <div class="card-enfermedad">
                <div class="imagen-placeholder"></div>
                <h3 class="titulo-azul">Tinnitus (Zumbido)</h3>
                <p>Percepción de sonidos (zumbidos, pitidos) sin una fuente externa. Puede ser síntoma de otras condiciones auditivas.</p>
                <strong>Síntomas principales:</strong>
                <ul>
                    <li>Zumbido constante o intermitente</li>
                    <li>Pitidos agudos</li>
                    <li>Ruido como cascada</li>
                    <li>Dificultad para concentrarse</li>
                </ul>
            </div>

            <!-- Tarjeta 4 -->
            <div class="card-enfermedad">
                <div class="imagen-placeholder"></div>
                <h3 class="titulo-azul">Vértigo</h3>
                <p>Sensación de que todo gira alrededor. Relacionado con problemas del oído interno que controla el equilibrio.</p>
                <strong>Síntomas principales:</strong>
                <ul>
                    <li>Mareos intensos</li>
                    <li>Náuseas y vómitos</li>
                    <li>Pérdida de equilibrio</li>
                    <li>Sudoración</li>
                </ul>
            </div>

